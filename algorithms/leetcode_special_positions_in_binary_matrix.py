from typing import List


def num_special(mat: List[List[int]]) -> int:
    n = len(mat)
    m = len(mat[0])
    row = [0] * n
    col = [0] * m

    for i in range(n):
        for j in range(m):
            if mat[i][j] == 1:
                row[i] += 1
                col[j] += 1

    result = 0
    for i in range(n):
        if row[i] == 1:
            for j in range(m):
                if col[j] == 1 and mat[i][j] == 1:
                    result += 1
    return result


